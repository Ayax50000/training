require 'creditcard'
require "rspec/expectations"


describe Creditcard do
  
  it "should create a credit card object" 

  it "should have a name" 

  context "when the creditcard is from Banamex" do

    it "should have a number" 

    it "should have 3 digit validation" 

  end

  context "when the creditcard is from HSBC" do

    it "should have a number" 

    it "should have 4 digit validation" 

  end

  context "when the creditcard is from Bancomer" do

    it "should have a number" 

    it "should have 5 digit validation" 

  end

  it "should have an expiration date" 

end
